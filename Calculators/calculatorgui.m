mb=get(handles.togglebutton5,'value')
if mb
    set(handles.edit73,'String','sin')
else
    set(handles.edit73,'String','cos')
end

hd=get(handles.edit66,'String')
% hp=get(handles.edit73,'String')
if hd=='DEG'
   cd= get(handles.edit73,'String')
    if cd=='sin'
        bc=get(handles.input,'String')
        gs=str2double(bc)
        fs=sind(gs)
        set(handles.answer,'String',fs)
    elseif cd=='cos'
        mz=get(handles.input,'String')
        tr=str2double(mz)
        kk=cosd(tr)
        set(handles.answer,'String',kk)
    end
else hd=='RAD'
    cd= get(handles.edit73,'String')
    if cd=='sin'
        bc=get(handles.input,'String')
        cc=str2double(bc)
        fs=sin(cc)
        set(handles.answer,'String',fs)
    elseif cd=='cos'
        mz=get(handles.input,'String')
        mk=str2double(mz)
        kk=cos(mk)
        set(handles.answer,'String',kk)
    end
end
